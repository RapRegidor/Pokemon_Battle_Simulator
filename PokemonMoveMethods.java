  public static int IcicleSpear() 
  {
    int[] array = new int[5];
    
    for(int i = 0; i < array.length; i++)
    {
      array[i] = (int)(Math.random()*10) + 10;
    }
    
    while(inArray(array, 13)) //13 is a cursed number 
    {
      for(int i = 0; i < array.length; i++)
      {
        array[i] = (int)(Math.random()*10) + 10;
      }
    }
    int average = meanArray(array);
    int common = modeNum(array);
    
    int randomChance = (int)(Math.random()*10) + 1;
    if(randomChance < 6)
    {
      return (average*5);
    }
    return (common*5);
  }
  
  public static boolean divisible(int x, int y)
  {
    if (x % y == 0 || x % y == 0)
    {
      return true;
    }
    return false;
  }
  
  public static int sumArray(int[] array) 
  {
    int result = 0;
    for(int i:array)
    {
      result += i;
    }
    return result;
  }
  
  public static int meanArray(int[] array) 
  {
    int sum = sumArray(array);
    return (int)(sum / array.length);
  }
  
  public static int modeNum(int[] array) 
  {
    int currentMode = array[0];
    int oldFrequency = 0;
    for(int i:array)
    {
      int currentFrequency = 0;
      for(int y:array)
      {
        if(i == y)
        {
          currentFrequency++;
        }
      }
      if(currentFrequency > oldFrequency)
      {
        currentMode = i;
        oldFrequency = currentFrequency;
      }
    }
    return currentMode;
  }
  
  public static boolean inArray(int[] array, int num) 
  { 
    boolean Appeared = false;
    for(int i:array)
    {
      if(i == num)
      {
        Appeared = true;
      }
    }
    return Appeared;
  }
